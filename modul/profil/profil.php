<p align="justify" class="style1">	Dorayaki qu, Berawal dari Kesukaan Anak Terhadap Dorayaki
 
Bagi Anda pecinta serial kartun Doraemon, pasti mengenal makanan kesukaan Doraemon yaitu dorayaki. Dalam serialnya Doraemon sangat menyukai dorayaki dan sanggup menghabiskan banyak dorayaki.
 
Tentu banyak penonton awalnya merasa penasaran dengan makanan  asal negeri sakura yang satu ini.
 
Kini dorayaki bukan suatu hal yang langka lagi. Dorayaki saat ini sudah banyak dijual di Indonesia, khususnya malang batu dan sekitarnya.
 
alfa, adalah salah satu yang membuka usaha menjual dorayaki di batu.  Berawal dari anaknya yang suka makan dorayak, alfa membangun usaha ini. Pemasarannya pun masih dari mulut kemulut. </p>

<p align="justify" class="style1">Usaha dorayaki alfa diberi merk sesuai dengan nama anaknya Celine. Begitu juga dengan logo pada bungkus dorayaki. Celine Dorayaki sudah beroperasi hampir satu tahun ini.
 
Dalam sehari Celine Dorayaki mampu memproduksi hingga lebih dari 500 buah dorayaki. Harga perbungkusnya pun cukup terjangkau, hanya Rp7.000. Pada akhir pekan produksi Celine Dorayaki meningkat karena pesanan yang lebih ramai dari biasanya.
 
Celine Dorayaki memiliki berbagai macam varian rasa seperti choco ovomaltine milky, oreo milky, ceres cheesy, chocolate double, cheese almond, choco tiramisu humble, dan chocolate green tea. </p>

<p align="justify" class="style1">Tekstur dari dorayaki ini sangat lembut, rasanya cokelat dan varian rasa lainnya sangat menyatu dengan lapisan dorayaki. Ukurannya pun cukup untuk mengenyangkan perut para penikmatnya.
 
Untuk dapat menikmati Celine Dorayaki ini, dapat langsung datang ke toko-toko kue seperti di Jajanan malang Saya Suka yang terletak di Jl. batu, TCSS yang berada di Jl. Durian, dan di beberapa toko lainnya. Selain itu pembeli dapat langsung datang kerumah produksi yang berada di jalan soekarno hatta No.2 </p> 

